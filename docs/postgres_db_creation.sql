-- F01: purge expired/revoked refresh tokens older than N days
CREATE OR REPLACE FUNCTION fn_cleanup_expired_tokens(retention_days INT DEFAULT 7)
RETURNS INTEGER AS $$
DECLARE
    cutoff  TIMESTAMP := (now() AT TIME ZONE 'utc') - make_interval(days => retention_days);
    deleted INTEGER;
BEGIN
    DELETE FROM refresh_tokens
    WHERE expires_at < cutoff
       OR (revoked_at IS NOT NULL AND revoked_at < cutoff);
    GET DIAGNOSTICS deleted = ROW_COUNT;
    RETURN deleted;
END;
$$ LANGUAGE plpgsql;

-- F02: purge intraday snapshots older than N days (option rows cascade)
CREATE OR REPLACE FUNCTION fn_purge_old_snapshots(retain_days INT DEFAULT 30)
RETURNS INTEGER AS $$
DECLARE
    cutoff  DATE := ((now() AT TIME ZONE 'utc')::date - retain_days);
    deleted INTEGER;
    total   INTEGER := 0;
BEGIN
    DELETE FROM option_chain_snapshots WHERE trade_date < cutoff;
    GET DIAGNOSTICS deleted = ROW_COUNT; total := total + deleted;

    DELETE FROM index_live_data WHERE trade_date < cutoff;
    GET DIAGNOSTICS deleted = ROW_COUNT; total := total + deleted;

    RETURN total;
END;
$$ LANGUAGE plpgsql;


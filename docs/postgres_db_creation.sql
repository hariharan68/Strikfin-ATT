ALTER TABLE instruments ADD COLUMN IF NOT EXISTS vendor_symbols     JSONB NOT NULL DEFAULT '{}'::jsonb;
ALTER TABLE instruments ADD COLUMN IF NOT EXISTS snapshot_enabled   BOOLEAN NOT NULL DEFAULT TRUE;
ALTER TABLE instruments ADD COLUMN IF NOT EXISTS status             VARCHAR(20) NOT NULL DEFAULT 'ACTIVE';


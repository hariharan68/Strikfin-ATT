    CONSTRAINT ck_ats_bias       CHECK (bias IN (-1, 0, 1)),
    CONSTRAINT ck_ats_confidence CHECK (confidence BETWEEN 0.0 AND 1.0),
    CONSTRAINT ck_ats_rr         CHECK (risk_reward IS NULL OR risk_reward >= 0)

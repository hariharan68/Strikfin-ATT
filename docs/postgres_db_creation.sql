ALTER TABLE instruments ADD COLUMN IF NOT EXISTS segment            VARCHAR(20);
ALTER TABLE instruments ADD COLUMN IF NOT EXISTS instrument_type    VARCHAR(20);
ALTER TABLE instruments ADD COLUMN IF NOT EXISTS underlying         VARCHAR(40);

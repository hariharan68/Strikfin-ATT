CREATE INDEX IF NOT EXISTS ix_ai_signals_asof
    ON ai_trade_signals (instrument_id, as_of);

-- ============================================================================
-- 12. SIGNAL OUTCOMES  (accuracy tracking — closes the feedback loop)
-- ============================================================================
CREATE TABLE IF NOT EXISTS signal_outcomes (
    id            BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    signal_id     BIGINT       NOT NULL,
    instrument_id SMALLINT     NOT NULL,
    bias          SMALLINT     NOT NULL,   -- 1 | 0 | -1
    status        VARCHAR(12)  NOT NULL,   -- OPEN|WIN|LOSS|EXPIRED|NEUTRAL

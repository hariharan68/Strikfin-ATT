    CONSTRAINT fk_outcome_instrument FOREIGN KEY (instrument_id)
        REFERENCES instruments (instrument_id)
);
CREATE INDEX IF NOT EXISTS ix_outcome_lookup ON signal_outcomes (instrument_id, status);

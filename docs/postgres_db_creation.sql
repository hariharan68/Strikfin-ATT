-- ============================================================================
-- 4. INDEX LIVE DATA  (append-only 1-min snapshots)
-- ============================================================================
CREATE TABLE IF NOT EXISTS index_live_data (
    row_id        BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    instrument_id SMALLINT      NOT NULL,
    trade_date    DATE          NOT NULL,
    snap_ts       TIMESTAMP     NOT NULL,
    last_price    NUMERIC(12,2) NOT NULL,
    open_price    NUMERIC(12,2),
    high_price    NUMERIC(12,2),
    low_price     NUMERIC(12,2),
    prev_close    NUMERIC(12,2),
    change_pct    NUMERIC(7,3),
    volume        BIGINT,
    india_vix     NUMERIC(7,3),

    CONSTRAINT fk_ild_instrument FOREIGN KEY (instrument_id)
        REFERENCES instruments (instrument_id),
    CONSTRAINT ck_ild_prices CHECK (
        last_price > 0
        AND (high_price IS NULL OR low_price IS NULL OR high_price >= low_price)
    ),
    CONSTRAINT ck_ild_india_vix CHECK (india_vix IS NULL OR india_vix > 0)
);
CREATE INDEX IF NOT EXISTS ix_index_live_data_lookup
    ON index_live_data (instrument_id, trade_date, snap_ts);

-- ============================================================================
-- 5. OPTION CHAIN SNAPSHOTS  (parent of option_chain_rows)
--    future_price: tradable current-month FUTURES price captured with the
--    snapshot. Options Lab price overlays (Multi OI & Volume, etc.) plot the
--    FUTURES price — the instrument traders actually deal — not the index spot.
--    Nullable: rows where the futures fetch failed fall back to `spot` on read
--    (see app/services/options_lab_service.py :: _fut_of / get_oi_series).
--    lot_size: the SEBI contract lot IN EFFECT at capture — frozen so a future
--    lot change never rescales historical lot-scaled reads. NULL (pre-column)
--    rows fall back to the instrument master (options_lab_service._lot_of).
-- ============================================================================
CREATE TABLE IF NOT EXISTS option_chain_snapshots (
    snapshot_id     BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    instrument_id   SMALLINT      NOT NULL,
    trade_date      DATE          NOT NULL,
    expiry_date     DATE          NOT NULL,
    snap_ts         TIMESTAMP     NOT NULL,
    spot            NUMERIC(12,2) NOT NULL,
    future_price    NUMERIC(12,2),
    lot_size        INTEGER,
    atm_strike      NUMERIC(12,2) NOT NULL,
    total_call_oi   BIGINT,
    total_put_oi    BIGINT,
    pcr_oi          NUMERIC(8,4),
    pcr_volume      NUMERIC(8,4),
    max_pain_strike NUMERIC(12,2),

    CONSTRAINT fk_ocs_instrument FOREIGN KEY (instrument_id)
        REFERENCES instruments (instrument_id),
    CONSTRAINT ck_ocs_spot         CHECK (spot > 0),
    CONSTRAINT ck_ocs_future_price CHECK (future_price IS NULL OR future_price > 0),
    CONSTRAINT ck_ocs_lot_size     CHECK (lot_size IS NULL OR lot_size > 0),
    CONSTRAINT ck_ocs_expiry       CHECK (expiry_date >= trade_date),
    CONSTRAINT ck_ocs_pcr          CHECK (pcr_oi IS NULL OR pcr_oi >= 0)
);
CREATE INDEX IF NOT EXISTS ix_oc_snapshots_lookup
    ON option_chain_snapshots (instrument_id, trade_date, snap_ts);

-- Idempotent backfill: CREATE TABLE above is skipped if the table already
-- exists, so re-running this script on a pre-future_price database still adds
-- the column (and its check) rather than silently leaving it behind.
ALTER TABLE option_chain_snapshots ADD COLUMN IF NOT EXISTS future_price NUMERIC(12,2);
ALTER TABLE option_chain_snapshots ADD COLUMN IF NOT EXISTS lot_size     INTEGER;
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_ocs_future_price') THEN
        ALTER TABLE option_chain_snapshots
            ADD CONSTRAINT ck_ocs_future_price CHECK (future_price IS NULL OR future_price > 0);
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_ocs_lot_size') THEN
        ALTER TABLE option_chain_snapshots
            ADD CONSTRAINT ck_ocs_lot_size CHECK (lot_size IS NULL OR lot_size > 0);
    END IF;
END $$;

-- ============================================================================
-- 6. OPTION CHAIN ROWS  (per-strike detail; cascade-deleted with snapshot)
-- ============================================================================
CREATE TABLE IF NOT EXISTS option_chain_rows (
    row_id       BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    snapshot_id  BIGINT        NOT NULL,
    trade_date   DATE          NOT NULL,
    strike       NUMERIC(12,2) NOT NULL,
    option_type  CHAR(2)       NOT NULL,
    ltp          NUMERIC(12,2),
    oi           BIGINT,
    oi_change    BIGINT,
    volume       BIGINT,
    iv           NUMERIC(7,3),
    delta        NUMERIC(7,4),
    theta        NUMERIC(9,4),
    vega         NUMERIC(9,4),
    gamma        NUMERIC(9,6),
    buildup_type SMALLINT,   -- 1=LongBuildup 2=ShortBuildup 3=ShortCovering 4=LongUnwinding

    CONSTRAINT fk_ocr_snapshot FOREIGN KEY (snapshot_id)
        REFERENCES option_chain_snapshots (snapshot_id) ON DELETE CASCADE,
    CONSTRAINT ck_ocr_option_type CHECK (option_type IN ('CE', 'PE')),
    CONSTRAINT ck_ocr_buildup CHECK (buildup_type IS NULL OR buildup_type BETWEEN 1 AND 4),
    CONSTRAINT ck_ocr_iv      CHECK (iv IS NULL OR iv > 0),
    CONSTRAINT ck_ocr_strike  CHECK (strike > 0)
);
CREATE INDEX IF NOT EXISTS ix_oc_rows_snap
    ON option_chain_rows (snapshot_id, option_type, strike);


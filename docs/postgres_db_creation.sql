/*
================================================================================
  STRIKFIN — PostgreSQL Database Creation Script
  Database : StrikfinDB
  Engine   : PostgreSQL 16+  (tested on 18)
  Driver   : SQLAlchemy 2.0 async + asyncpg  (postgresql+asyncpg://)
  Auth     : role + password (set DB_USER / DB_PASSWORD in backend/.env)
================================================================================

  HOW TO USE (step by step)
  ─────────────────────────
  1. Make sure PostgreSQL 16+ is running and you know the port (default 5432).
  2. Create the database FIRST (this cannot be done from inside it):

         CREATE DATABASE "StrikfinDB";

     (Keep the double quotes — Postgres folds unquoted names to lowercase,
      so quoting preserves the capital S and D used by the app.)

  3. Connect to StrikfinDB, then run THIS script:
         • pgAdmin : select StrikfinDB → Query Tool → open this file → Run (F5)
         • psql    : psql -h localhost -p 5432 -U postgres -d StrikfinDB -f docs/postgres_db_creation.sql

  4. Point the app at it via backend/.env:
         DB_HOST=localhost
         DB_PORT=5432
         DB_NAME=StrikfinDB
         DB_USER=postgres
         DB_PASSWORD=your-postgres-password

  NOTE: In development the backend also auto-creates these tables from the ORM
  (app/db/models.py) on startup, and Alembic manages them in production. This
  script is the manual / reference equivalent and is safe to re-run (every
  statement uses IF NOT EXISTS / ON CONFLICT).

  TABLE INVENTORY (execution order — mirrors app/db/models.py)
  ───────────────────────────────────────────────────────────
  01. users
  02. refresh_tokens
  02b. user_preferences          (1:1 with users — Settings-page state)
  02c. broker_connections        (per-user encrypted vendor tokens)
  03. instruments                (seeded: NIFTY50, SENSEX)
  04. index_live_data
  05. option_chain_snapshots
  06. option_chain_rows
  07. institutional_activity
  08. news_feed
  09. market_sentiment
  10. smart_money_signals
  11. ai_trade_signals
  12. signal_outcomes
  13. audit_logs
  14. MULTI-TENANT (SaaS) — M5:
      permissions · plans · roles · organizations · role_permissions ·
      api_keys · memberships · subscriptions
      (seeded reference data: 13 permissions, 4 roles + grants, 4 plans; + RLS)

  VIEWS
  ─────────────────────────
  V01. vw_latest_index_snapshot
  V02. vw_latest_option_snapshot
  V03. vw_daily_institutional_summary

  FUNCTIONS (maintenance — replace the old T-SQL stored procedures)
  ─────────────────────────
  F01. fn_cleanup_expired_tokens(retention_days INT)
  F02. fn_purge_old_snapshots(retain_days INT)

  DRIFT NOTE: the legacy MSSQL script also had a `market_regime` table +
  `vw_latest_regime` view, but there is no matching ORM model in models.py
  today, so they are intentionally omitted here. Reconcile in "Part 2"
  (re-add the ORM model or formally drop the feature) before relying on it.
================================================================================
*/

-- ============================================================================
-- 1. USERS
-- ============================================================================
CREATE TABLE IF NOT EXISTS users (
    user_id       BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email         VARCHAR(255)  NOT NULL,
    password_hash VARCHAR(255)  NOT NULL,
    display_name  VARCHAR(100),
    phone         VARCHAR(20),
    state         VARCHAR(64),
    auth_provider VARCHAR(20)   NOT NULL DEFAULT 'email',
    is_active     BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at    TIMESTAMP     NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),
    last_login_at TIMESTAMP,

    CONSTRAINT uq_users_email     UNIQUE (email),
    CONSTRAINT ck_users_email_len CHECK (char_length(email) >= 3)
);
CREATE INDEX IF NOT EXISTS ix_users_email ON users (email);

-- Idempotent backfill: CREATE TABLE above is skipped if `users` already exists,
-- so re-running this script on a pre-profile database still adds the Settings-
-- page profile columns rather than silently leaving them behind.
ALTER TABLE users ADD COLUMN IF NOT EXISTS phone         VARCHAR(20);
ALTER TABLE users ADD COLUMN IF NOT EXISTS state         VARCHAR(64);
ALTER TABLE users ADD COLUMN IF NOT EXISTS auth_provider VARCHAR(20) NOT NULL DEFAULT 'email';

-- ============================================================================
-- 2. REFRESH TOKENS
-- ============================================================================
CREATE TABLE IF NOT EXISTS refresh_tokens (
    token_id    BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT        NOT NULL,
    token_hash  VARCHAR(255)  NOT NULL,
    expires_at  TIMESTAMP     NOT NULL,
    revoked_at  TIMESTAMP,
    device_info VARCHAR(300),
    created_at  TIMESTAMP     NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),

    CONSTRAINT uq_refresh_tokens_hash UNIQUE (token_hash),
    CONSTRAINT fk_refresh_tokens_user FOREIGN KEY (user_id)
        REFERENCES users (user_id) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS ix_refresh_tokens_user ON refresh_tokens (user_id);

-- ============================================================================
-- 2b. USER PREFERENCES  (1:1 with users — Settings-page state, cascade-deleted)
-- ============================================================================
CREATE TABLE IF NOT EXISTS user_preferences (
    user_id            BIGINT       PRIMARY KEY,
    theme              VARCHAR(20),                              -- classic|warm|dark|terminal
    show_chart_tooltip BOOLEAN      NOT NULL DEFAULT TRUE,
    call_put_scheme    VARCHAR(16)  NOT NULL DEFAULT 'classic',  -- classic|inverted
    created_at         TIMESTAMP    NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),
    updated_at         TIMESTAMP    NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),

    CONSTRAINT fk_user_preferences_user FOREIGN KEY (user_id)
        REFERENCES users (user_id) ON DELETE CASCADE
);

-- ============================================================================
-- 2c. BROKER CONNECTIONS  (per-user encrypted vendor tokens — Fyers today)
-- ============================================================================
-- Access/refresh tokens are Fernet-encrypted (never plaintext). user_id is
-- nullable: the Fyers OAuth callback is unauthenticated, so that row is the
-- implicit single-user/global connection until per-user M5 wiring lands.
CREATE TABLE IF NOT EXISTS broker_connections (
    id                UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id           BIGINT,
    broker            VARCHAR(20)   NOT NULL,           -- fyers | zerodha | …
    access_token_enc  TEXT,
    refresh_token_enc TEXT,
    meta              JSONB         NOT NULL DEFAULT '{}'::jsonb,
    status            VARCHAR(20)   NOT NULL DEFAULT 'ACTIVE',  -- ACTIVE|EXPIRED|REVOKED
    generated_at      TIMESTAMP,
    expires_at        TIMESTAMP,
    created_at        TIMESTAMP     NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),
    updated_at        TIMESTAMP     NOT NULL DEFAULT (now() AT TIME ZONE 'utc'),

    CONSTRAINT fk_broker_conn_user FOREIGN KEY (user_id)
        REFERENCES users (user_id) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS ix_broker_conn_user_broker ON broker_connections (user_id, broker);


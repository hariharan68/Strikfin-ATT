================================================================================
  PART 2 — OPTIONAL POSTGRESQL IMPROVEMENTS (apply via Alembic, not here)
================================================================================
  These are the scaling upgrades discussed during the MSSQL → PostgreSQL move.
  They are intentionally NOT baked into the script above so it stays a faithful
  1:1 of the current ORM. Apply them as a follow-up migration:

  1. JSONB instead of TEXT for JSON columns (queryable + indexable):
       ALTER TABLE smart_money_signals ALTER COLUMN evidence TYPE JSONB USING evidence::jsonb;
       ALTER TABLE audit_logs          ALTER COLUMN detail   TYPE JSONB USING detail::jsonb;

  2. Timezone-aware timestamps (the app stores UTC; make it explicit):
       -- change DateTime -> DateTime(timezone=True) in models.py, then
       ALTER TABLE users ALTER COLUMN created_at TYPE TIMESTAMPTZ
            USING created_at AT TIME ZONE 'utc';
       -- ...repeat for every TIMESTAMP column.

  3. VARCHAR instead of CHAR for fixed-code columns (avoid blank-padding):
       ALTER TABLE option_chain_rows   ALTER COLUMN option_type TYPE VARCHAR(2);
       ALTER TABLE smart_money_signals ALTER COLUMN option_type TYPE VARCHAR(2);
       ALTER TABLE news_feed           ALTER COLUMN dedup_hash  TYPE VARCHAR(64);

  4. Range partitioning by trade_date on the high-volume append-only tables
     (index_live_data, option_chain_rows) once row counts exceed a few million.

  5. Schedule maintenance with pg_cron (or Windows Task Scheduler):
       SELECT cron.schedule('cleanup-tokens', '0 3 * * *',  $$SELECT fn_cleanup_expired_tokens(7)$$);
       SELECT cron.schedule('purge-snapshots','0 4 * * 0',  $$SELECT fn_purge_old_snapshots(30)$$);
